    <!-- Main Content for Page -->
    <main id="history">
      <p>
        The idea of a National Park on the east coast had been thrown around
        since the early 1900s but never took hold until National Park Service
        Director Stephen Mather approached the government looking to establish a
        park in the Southern Appalachians. At the same time a group of
        businessmen in the Shenandoah Valley were rallying to establish a park
        in the area to show off the beauty of their home.
      </p>

      <p>
        When choosing a location to build the east coast's first national park,
        congress was between the Great Smoky Mountains, Mammoth Cave, and
        Shenandoah Valley. Shenandoah Valley was chosen out of the three and a
        campaign began to purchase the land needed to build the park. Congress
        had set a precedent of only establishing parks on government owned land
        and demanded that the land for Shenandoah National Park be government
        owned as well.
      </p>

      <p>
        The Shenandoah National Park Association aimed to raise $2.5 million to
        purchase all 400,000 acres of proposed land the park would occupy. They
        managed to raise half of that and as a result had to cut the acreage
        down to 327,000. With that money they purchased an estimated 4,000
        properties within the boundaries of the park.
      </p>

      <p>
        When construction started on the park in 1931 the park consisted of
        Skyline Drive and land from White Oak Canyons and Big Meadows. To aid
        with the park's construction, FDR established the Civilian Conservation
        Corps in 1933. The Civilian Corps were a huge success, boosting
        productivity and creating jobs during the Great Depression. The Civilian
        Corps were hugely influential on the outcome of the park.
      </p>

      <p>
        During construction Congress had to relocate over 400 families living in
        the Blue Ridge Mountains. Initially brushing off the issue, Congress was
        forced to buy over 6,000 acres outside of the park for relocation
        following public outcry. Around half of the families were relocated to
        these new properties while half of the families simply left to find a
        new place themselves.
      </p>
    </main>
